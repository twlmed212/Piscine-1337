#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	temp;

	temp = 0;
	if (nb < 0)
	{
		if (nb == -2147483648)
		{
			write(1, "-2147483648", 10);
			return;
		}
		nb *= -1;
		write(1, "-", 1);
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
	}
	temp = (nb % 10) + '0';
	write(1, &temp, 1);
}
