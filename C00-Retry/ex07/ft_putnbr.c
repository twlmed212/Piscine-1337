#include <unistd.h>
#include <stdbool.h>
#include <stdio.h>

void	ft_putnbr(int nb)
{
	char	save;

	if (nb < 0)
	{
		write(1, "-", 1);
		if (nb == -2147483648)
		{
			write(1, "214748364", 9);
			nb = -8;
		}
		nb = nb * -1;
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
	}
	save = nb % 10 + '0';
	write(1, &save, 1);
}
